/*
 * Dati due caratteri, restituire la somma dei loro valori ASCII
 * */

package com.develhope.basics.casting;

public class CharAscii {
    public static void main(String[] args) {
        char char1 = 'a';
        char char2 = 'b';
        int charAscii = char1 + char2;

        System.out.println(charAscii);
    }
}